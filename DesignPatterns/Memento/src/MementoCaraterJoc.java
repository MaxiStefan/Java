
public class MementoCaraterJoc {
	String nume;
	int puncteViata;
	
	public MementoCaraterJoc(String nume, int puncteViata) {
		super();
		this.nume = nume;
		this.puncteViata = puncteViata;
	}

	public String getNume() {
		return nume;
	}

	public int getPuncteViata() {
		return puncteViata;
	}
	
	
}
