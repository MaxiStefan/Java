package Hospital.ChainMax;

public class Patient {
	String name;
	float temperature;
	boolean isBleeding;
	boolean isUnconscious;
	
	public Patient(String name, float temperature, boolean isBleeding, boolean isUnconscious) {
		super();
		this.name = name;
		this.temperature = temperature;
		this.isBleeding = isBleeding;
		this.isUnconscious = isUnconscious;
	}
	
	
}
